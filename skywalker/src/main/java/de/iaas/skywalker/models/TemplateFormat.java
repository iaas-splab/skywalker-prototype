package de.iaas.skywalker.models;

public class TemplateFormat {
    public String fileFormat;
    public String templateFileBody;

    public String getFileFormat() {
        return fileFormat;
    }

    public void setFileFormat(String fileFormat) {
        this.fileFormat = fileFormat;
    }

    public String getTemplateFileBody() {
        return templateFileBody;
    }

    public void setTemplateFileBody(String templateFileBody) {
        this.templateFileBody = templateFileBody;
    }
}
